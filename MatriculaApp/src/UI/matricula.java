    private boolean checkEmptyNames(){
        return namesTextField.getText().trim().isEmpty();
    }
    
    private boolean checkEmptySurnames(){
        return surnamesTextField.getText().trim().isEmpty();
    }
    
    private boolean checkEmptyRUT(){
        return rutTextField.getText().trim().isEmpty();
    }
    
    private boolean checkGenreMale(){
        return jRadioButton1.isSelected();
    }
    
    private boolean checkGenreFemale(){
        return jRadioButton2.isSelected();
    }
    
    private boolean checkGenreOther(){
        return jRadioButton3.isSelected();
    }
    
    private boolean checkEmptyBirth(){
        return birthTextField.getText().trim().isEmpty();
    }
    
    private boolean checkEmptyPhone(){
        return phoneTextField.getText().trim().isEmpty();
    }
    
    private boolean checkEmptyRegion(){
        return regionTextField.getText().trim().isEmpty();
    }
    
    private boolean checkEmptyCity(){
        return cityTextField.getText().trim().isEmpty();
    }
    
    private boolean checkFieldsEmpty(){
        return checkEmptyNames()&& checkEmptySurnames()&&checkEmptyRUT()
                &&checkEmptyBirth()&&checkEmptyPhone()&&checkEmptyRegion()
                &&checkEmptyCity();
    }
    
    
