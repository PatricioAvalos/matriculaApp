    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(checkFieldsEmpty()){
            messageLabel.setText("Rellene los campos");
        }
        else{
            try(FileWriter fw = new FileWriter("matricula.txt", true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter out = new PrintWriter(bw)){
                if(checkGenreMale()){
                    out.println(namesTextField.getText()+";"+surnamesTextField.getText()
                    +rutTextField.getText()+";"+"Masculino"+";"+birthTextField.getText()+
                    ";"+phoneTextField.getText()+";"+regionTextField.getText()+";"+cityTextField.getText());
                    messageLabel.setText("Estudiante Añadido");
                }
                else if(checkGenreFemale()){
                    out.println(namesTextField.getText()+";"+surnamesTextField.getText()
                    +rutTextField.getText()+";"+"Femenino"+";"+birthTextField.getText()+
                    ";"+phoneTextField.getText()+";"+regionTextField.getText()+";"+cityTextField.getText());
                    messageLabel.setText("Estudiante Añadido");
                }
                else if(checkGenreOther()){
                    out.println(namesTextField.getText()+";"+surnamesTextField.getText()
                    +rutTextField.getText()+";"+"Otro"+";"+birthTextField.getText()+
                    ";"+phoneTextField.getText()+";"+regionTextField.getText()+";"+cityTextField.getText());
                    messageLabel.setText("Estudiante Añadido");
                }
                else{messageLabel.setText("Marque Género");}
            }catch (IOException e) {
             }
    
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!checkFieldsEmpty()){
            int ax = JOptionPane.showConfirmDialog(null, "¿Deseas Salir de Matrícula?");
            if(ax == JOptionPane.YES_OPTION){dispose();
                functions functions = new functions(usernameLogin);
                functions.setVisible(true);
            }
        }
        else{
            dispose();
            functions functions = new functions(usernameLogin);
            functions.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
